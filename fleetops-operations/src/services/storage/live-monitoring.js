function createVehicle(config) {
    return {
        playbackPath: buildPlaybackPath(config.pos),
        ...config,
    };
}

function buildTimeline(entries) {
    return entries.map(([time, title, location]) => ({
        time,
        title,
        location,
    }));
}

function buildPlaybackPath(anchor) {
    const [x, y] = anchor;
    return [
        [x - 10, y - 8],
        [x - 4, y - 5],
        [x + 2, y - 2],
        [x + 7, y + 1],
        [x, y],
    ];
}
